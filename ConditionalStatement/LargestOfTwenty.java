package ConditionalStatement;

public class LargestOfTwenty {
    public static void main(String args[])
    {
        int A=10;
        int B=50;
        if(A>=B)
        {
            System.out.println("A is largest of 20");
        }
        else
        {
            System.out.println("B is largest of 20");
        }
    }
    
}
