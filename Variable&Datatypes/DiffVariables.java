public class DiffVariables {
    public static void main(String args[])
    {
        int a=10;
        int b=5;
        System.out.println(a);
        System.out.println(b);
        String name="Ankita Pandey";
        System.out.println(name);
        a=60;
        System.out.println(a);
        a=b;
        System.out.println(a);

    }
}
